package framework.controller.bean;

public class ColumnsExportBean {
	private String width;
	private String label;
	private String labelKey;
	private String field;
	private String type;
	private String sortable;
	private String filtrable;
	private String formatable;
	private Object groupValues;

	/**
	 * @return the width
	 */
	public String getWidth() {
		return width;
	}
	/**
	 * @param width the width to set
	 */
	public void setWidth(String width) {
		this.width = width;
	}
	/**
	 * @return the value
	 */
	public String getLabel() {
		return label;
	}
	/**
	 * @param value the value to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}
	/**
	 * @return the field
	 */
	public String getField() {
		return field;
	}
	/**
	 * @param field the field to set
	 */
	public void setField(String field) {
		this.field = field;
	}
	/**
	 * @return the sortable
	 */
	public String isSortable() {
		return sortable;
	}
	/**
	 * @param sortable the sortable to set
	 */
	public void setSortable(String sortable) {
		this.sortable = sortable;
	}
	/**
	 * @return the filtrable
	 */
	public String isFiltrable() {
		return filtrable;
	}
	/**
	 * @param filtrable the filtrable to set
	 */
	public void setFiltrable(String filtrable) {
		this.filtrable = filtrable;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	public Object getGroupValues() {
		return groupValues;
	}
	public void setGroupValues(Object groupValues) {
		this.groupValues = groupValues;
	}
	public String getLabelKey() {
		return labelKey;
	}
	public void setLabelKey(String labelKey) {
		this.labelKey = labelKey;
	}
	public String getFormatable() { 
		return formatable;
	}
	public void setFormatable(String formatable) {
		this.formatable = formatable;
	}
	
}