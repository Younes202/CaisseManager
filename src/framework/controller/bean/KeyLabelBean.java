package framework.controller.bean;

public class KeyLabelBean {
	private String key;
	private String label;
	
	/**
	 * @param key
	 * @param label
	 */
	public KeyLabelBean(String key, String label){
		this.key = key;
		this.label = label;
	}
	
	/**
	 * @return the key
	 */
	public String getKey() {
		return key;
	}
	/**
	 * @param key the key to set
	 */
	public void setKey(String key) {
		this.key = key;
	}
	/**
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}
	/**
	 * @param label the label to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}
}
