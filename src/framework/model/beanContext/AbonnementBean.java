	public String getAbonnementStr(){
		String recap = "";
		
		recap += "<b style='color:blue;'>PACK</b><br>";
		recap += StringUtil.getValueOrEmpty(packType)+"<br>";
		 
		recap += "<br><b style='color:blue;'>OPTIONS ET MODULES</b><br>";
		if(isOptCommercial){ recap += "Gestion commerciale<br>"; }
		if(isOptLivraison){ recap += "Gestion de livraisons<br>"; }
		if(isOptCompta){ recap += "Comptabilité<br>"; }
		if(isOptControle){ recap += "Contrôle de gestion<br>"; }
		if(isOptRh){ recap += "Resources humaine<br>"; }
		if(isOptStock){ recap += "Gestion de stock<br>"; }
		
		recap += "<br><b style='color:blue;'>OPTIONS PLUS</b><br>";
		if(isOptPlusBackup){ recap += "Sauvegarde des données locale et Cloud<br>"; } 
		if(isOptPlusEtsCentrale){ recap += "Considéré comme une centrale<br>"; }
		if(isOptPlusSyncCentrale){ recap += "Synchrinisation avec la centrale<br>"; }
		if(isOptPlusSynchroCloud){ recap += "Synchronisation avec le Cloud<br>"; }
		if(isOptPlusCmdVitrine){ recap += "Commande depuis site vitrine<br>"; }
		if(isOptPlusPageVitrine){ recap += "Page site vitrine<br>"; }
		if(isOptPlusRemote){ recap += "Accès distant<br>"; }
		if(isOptPlusOptimisation){ recap += "Optimisation des comptes<br>"; }
		
		recap += "<br><b style='color:blue;'>APPLICATIONS SATTELITES</b><br>";
		recap += "Caisse ("+getNbFormat(nbrCaisse)+")<br>";
		recap += "Caisse mobile ("+getNbFormat(nbrSatCaisseMob)+")<br>";
		if(isSatCaisseAuto){ recap += "Caisse autonome client ("+getNbFormat(nbrSatCaisseAuto)+")<br>"; }
		
		if(isSatAffCaisse){ recap += "Afficheur caisse ("+getNbFormat(nbrSatAffCaisse)+")<br>"; }
		if(isSatAffClient){ recap += "Afficheur client ("+getNbFormat(nbrSatAffClient)+")<br>"; }
		
		if(isSatCuisine){ recap += "Terminaux de cuisine ("+getNbFormat(nbrSatCuisine)+")<br>"; }
		if(isSatCuisine){ recap += "Terminal de pilotage ("+getNbFormat(nbrSatPilotage)+")<br>"; }
		if(isSatCuisine){ recap += "Terminaux de présentoire ("+getNbFormat(nbrSatPresentoire)+")<br>"; }
		
		if(isSatBalance){ recap += "Balance ("+getNbFormat(nbrSatBalance)+")<br>"; }
		if(isSatLecteurBarre){ recap += "Lecteur de prix ("+getNbFormat(nbrSatLecteurBarre)+")<br>"; }
		
		return recap;
	}
	private String getNbFormat(Integer nbr){
		if(nbr == null || nbr == 99 || nbr == 0){
			return "<span style='color:green;'>Illimité</span>"; 
		}
		return ""+nbr;
	}
