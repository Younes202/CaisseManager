package appli.controller.domaine.stock.bean;

import java.math.BigDecimal;

public class EmplInventaireBean{
	private BigDecimal quantite;
	private BigDecimal mttTtc;
	private Long emplacementId;
	private String emplacement;
	
	public BigDecimal getQuantite() {
		return quantite;
	}
	public void setQuantite(BigDecimal quantite) {
		this.quantite = quantite;
	}
	public BigDecimal getMttTtc() {
		return mttTtc;
	}
	public void setMttTtc(BigDecimal mttTtc) {
		this.mttTtc = mttTtc;
	}
	public Long getEmplacementId() {
		return emplacementId;
	}
	public void setEmplacementId(Long emplacementId) {
		this.emplacementId = emplacementId;
	}
	public String getEmplacement() {
		return emplacement;
	}
	public void setEmplacement(String emplacement) {
		this.emplacement = emplacement;
	}
}