package appli.controller.domaine.caisse_restau.bean;

import java.math.BigDecimal;
import java.util.List;

import appli.model.domaine.stock.persistant.ArticlePersistant;

public class MenuClientBean{
	private BigDecimal prix;
	private BigDecimal quantite;
	private String path;
	private String parent_libelle;
	private Long element_id;
	private String element_libelle;
	private List<ArticlePersistant> listArticle;
	
	public BigDecimal getPrix() {
		return prix;
	}
	public void setPrix(BigDecimal prix) {
		this.prix = prix;
	}
	public BigDecimal getQuantite() {
		return quantite;
	}
	public void setQuantite(BigDecimal quantite) {
		this.quantite = quantite;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getParent_libelle() {
		return parent_libelle;
	}
	public void setParent_libelle(String parent_libelle) {
		this.parent_libelle = parent_libelle;
	}
	public List<ArticlePersistant> getListArticle() {
		return listArticle;
	}
	public void setListArticle(List<ArticlePersistant> listArticle) {
		this.listArticle = listArticle;
	}
	public Long getElement_id() {
		return element_id;
	}
	public void setElement_id(Long element_id) {
		this.element_id = element_id;
	}
	public String getElement_libelle() {
		return element_libelle;
	}
	public void setElement_libelle(String element_libelle) {
		this.element_libelle = element_libelle;
	}
}