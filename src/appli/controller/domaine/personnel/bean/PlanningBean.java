package appli.controller.domaine.personnel.bean;

import appli.model.domaine.personnel.persistant.PlanningPersistant;
import framework.controller.bean.action.IViewBean;

public class PlanningBean extends PlanningPersistant implements IViewBean {
	private String[] employes_array;
	private String[] clients_array;
	private String[] lieu_array;

	public String[] getEmployes_array() {
		return employes_array;
	}
	public void setEmployes_array(String[] employes_array) {
		this.employes_array = employes_array;
	}
	public String[] getLieu_array() {
		return lieu_array;
	}
	public void setLieu_array(String[] lieu_array) {
		this.lieu_array = lieu_array;
	}
	public String[] getClients_array() {
		return clients_array;
	}
	public void setClients_array(String[] clients_array) {
		this.clients_array = clients_array;
	}
}
